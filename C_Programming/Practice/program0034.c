#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input         : int
//  Output        : Prints numbers from given input to 0 on console
//  Description   : Accepts a number as input and displays all numbers in
//                  descending order starting from the given number up to 0.
//  Date          : 24/06/2026
//  Author        : Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    // Input Updater
    if(iNo < 0)
    {
        printf("Invalid Input");
        return;
    }

    for(iCnt = iNo; iCnt >= 0; iCnt--)
    {
        printf("%d\n", iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Reverse Number Display Program
//  Description      : Accept a number from the user and display numbers
//                     from that number down to 0.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter the frequency : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

