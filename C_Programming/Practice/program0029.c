void Display(int iNo)
{
    int iCnt = 0;
    
    //Updater
    if(iNo < 0) 
    {
        iNo= -iNo;
    }
    
    for(iCnt = 1; iCnt<=iNo ; iCnt++)
    {
         printf("Jay Ganesh...\n");
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Prints "Jay Ganesh..." on console iNo times
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter the frequesncy");
    scanf("%d",&iValue);
   
   Display(iValue);
       
    return 0;
}   

