#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayDigits
//  Input         : int
//  Output        : Prints each digit of the given number
//  Description   : Accepts a positive integer as input and displays each digit
//                  of the number on a separate line starting from the last
//                  digit (rightmost digit) to the first digit (leftmost digit).
//  Date          : 04/07/2026
//  Author        : Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void DisplayDigits(int iNo)
{
    int iDigit = 0;

    while(iNo > 0)
    {
        iDigit = iNo % 10;
        printf("%d\n", iDigit);

        iNo = iNo / 10;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Digit Display Program
//  Description      : Accept a positive number from the user and display each
//                     digit of the number on a separate line in reverse order.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number : ");
    scanf("%d", &iValue);

    DisplayDigits(iValue);

    return 0;
}

