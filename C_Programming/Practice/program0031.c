#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function name : Display
//  Input :         int
//  Output :        Prints "Jay Ganesh..." with sequence numbers on console iNo times
//  Description :   Print's "Jay Ganesh..." given input times with sequence numbers
//  Date :          24/06/2026
//  Author :        Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;
    
    //Updater
    if(iNo < 0) 
    {
        printf("Invalid Input");
        return;
    }
    
    for(iCnt = 1; iCnt<=iNo ; iCnt++)
    {
         printf("%d : Jay Ganesh...\n",iCnt);
    }
}


///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Prints "Jay Ganesh..." with sequence numbers on console iNo times
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter the frequesncy");
    scanf("%d",&iValue);
   
    Display(iValue);
       
    return 0;
}  

