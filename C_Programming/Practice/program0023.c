#define AGE_INVALID -1

///////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function name : CalculateTicketPrice
//  Input :         int
//  Output :        int
//  Description :   Returns Ticket Price According to user age
//  Date :          22/06/2026
//  Author :        Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

int CalculateTicketPrice(int iAge)
{
    // Input Filter
    if(iAge < 0)
    {
        return AGE_INVALID;
    }

    if(iAge >= 0 && iAge <= 5)
    {
        return 0;
    }
    else if(iAge >= 6 && iAge <= 18)
    {
        return 500;
    }
    else if (iAge >= 19 && iAge <= 50)
    {
        return 900;
    }
    else 
    {
        return 400;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Calculate Ticket Price
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Please Enter your age to calculate ticket prices : ");
    scanf("%d",&iValue);

    iRet = CalculateTicketPrice(iValue);

    if (iRet == AGE_INVALID)
    {
        printf("Please enter positive Age\n");
    }
    else
    {
        printf("Your Ticket orice will be %d rs\n", iRet);
    }
    
    return 0;
} 

