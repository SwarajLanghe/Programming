#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input         : int
//  Output        : Prints numbers from 1 to given input on console
//  Description   : Displays numbers in ascending order starting from 1 up to
//                  the specified input value.
//  Date          : 24/06/2026
//  Author        : Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;
    
    //Updater
    if(iNo < 0) 
    {
        printf("Invalid Input");
        return;
    }
    
    for(iCnt = 1; iCnt<=iNo ; iCnt++)
    {
         printf("%d\n",iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Number Display Program
//  Description      : Accept a number from user and display numbers from
//                     1 to that number.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter the frequesncy : ");
    scanf("%d",&iValue);
   
   Display(iValue);
       
    return 0;
}   

