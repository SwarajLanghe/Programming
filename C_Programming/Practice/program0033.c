#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input         : int
//  Output        : Prints numbers from 0 to given input on console
//  Description   : Accepts a number as input and displays all numbers in
//                  ascending order starting from 0 up to the given number.
//  Date          : 24/06/2026
//  Author        : Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    // Input Updater
    if(iNo < 0)
    {
        printf("Invalid Input");
        return;
    }

    for(iCnt = 0; iCnt <= iNo; iCnt++)
    {
        printf("%d\n", iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Number Display Program
//  Description      : Accept a number from the user and display numbers
//                     from 0 to that number.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue;

    printf("Enter the frequency : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

