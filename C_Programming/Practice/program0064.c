#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input         : int
//  Output        : Prints numbers from negative input value to positive input value
//  Description   : Accepts a number as input and displays all numbers starting from the
//                  negative of the given number up to the positive of the given number.                
//  Date          : 30/06/2026
//  Author        : Swaraj Ashok Langhe
//
///////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    // Input Updater
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = -iNo; iCnt <= iNo; iCnt++)
    {
        printf("%d\t", iCnt);
    }

    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Range Display Program
//  Description      : Accept a number from the user and display all
//                     numbers from its negative value to its positive value.
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

